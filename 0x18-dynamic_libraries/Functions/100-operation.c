#include <stdio.h>

/**
 * add - adds integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: the results.
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substracts integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: the results.
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - divides integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: the results.
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multipies integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: the results.
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - gets the modules of integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: the results.
 */

int mod(int a, int b)
{
	return (a % b);
}

